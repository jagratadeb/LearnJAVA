public class Variables {
    public static void main(String[] args) {
        // declared a variable
        int a = 50;
        System.out.println(a);
        
        // changing the value of the variable
        a = 100;
        System.out.println(a);
        
        // variable name can start with $ sign
        String $ok;
        $ok = "how to be happy?";
        System.out.println($ok);
    }
}
